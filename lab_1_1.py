# 1. Считать с клавиатуры три произвольных числа,
# найти минимальное среди них и вывести на экран.
print("1. Поиск минимального из трех чисел")

a = float(input("Введите первое число: "))
b = float(input("Введите второе число: "))
c = float(input("Введите третье число: "))

minimum = a

if b < minimum:
    minimum = b

if c < minimum:
    minimum = c

print("Минимальное число:", minimum)
print()


# 2. Считать с клавиатуры три произвольных числа,
# вывести в консоль те числа, которые попадают в интервал [1, 50].
print("2. Числа из интервала [1, 50]")

a = float(input("Введите первое число: "))
b = float(input("Введите второе число: "))
c = float(input("Введите третье число: "))

found = False

if 1 <= a <= 50:
    print(a)
    found = True

if 1 <= b <= 50:
    print(b)
    found = True

if 1 <= c <= 50:
    print(c)
    found = True

if not found:
    print("Чисел из интервала [1, 50] нет")

print()


# 3. Считать с клавиатуры вещественное число m.
# Посчитать и вывести в консоль каждый член последовательности:
# [(1 * m), (2 * m), ..., (10 * m)].
# Решение через цикл for.
print("3. Последовательность (1 * m), (2 * m), ..., (10 * m)")

m = float(input("Введите вещественное число m: "))

for i in range(1, 11):
    print(i * m)

print()


# 4. Считать с клавиатуры непустую произвольную последовательность
# целых чисел. Найти сумму и количество чисел.
# Решение через цикл while.
print("4. Сумма и количество чисел последовательности")

total_sum = 0
count_numbers = 0

while True:
    number = int(input("Введите целое число: "))
    total_sum += number
    count_numbers += 1

    answer = input("Продолжить ввод? (1 - да, 0 - нет): ")

    if answer == "0":
        break

print("Сумма всех чисел:", total_sum)
print("Количество всех чисел:", count_numbers)